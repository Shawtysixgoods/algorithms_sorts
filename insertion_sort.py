# Сортировка вставками

def insertion_sort(arr):
    # Проходим по всем элементам начиная со второго (первый элемент считается отсортированным)
    for i in range(1, len(arr)):
        key = arr[i]  # Элемент, который нужно вставить в отсортированную часть
        j = i - 1  # Начинаем сравнивать с предыдущим элементом

        # Сдвигаем элементы в отсортированной части списка, пока не найдем место для key
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]  # Сдвигаем элемент вправо
            j -= 1  # Переходим к предыдущему элементу

        # Вставляем key на найденное место
        arr[j + 1] = key
    
    return arr

# Пример использования
numbers = [64, 34, 25, 12, 22, 11, 90]
print(insertion_sort(numbers))  # Вывод: [11, 12, 22, 25, 34, 64, 90]