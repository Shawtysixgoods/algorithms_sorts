# Быстрая сортировка

def quick_sort(arr):
    # Базовый случай: если список состоит из одного элемента или пустой, возвращаем его
    if len(arr) <= 1:
        return arr
    
    # Выбираем опорный элемент (в данном случае — средний элемент списка)
    pivot = arr[len(arr) // 2]
    
    # Разделяем список на три части:
    # 1) Элементы меньше опорного
    left = [x for x in arr if x < pivot]
    # 2) Элементы, равные опорному
    middle = [x for x in arr if x == pivot]
    # 3) Элементы больше опорного
    right = [x for x in arr if x > pivot]
    
    # Рекурсивно сортируем левую и правую части, а затем объединяем результат
    return quick_sort(left) + middle + quick_sort(right)

# Пример использования
numbers = [64, 34, 25, 12, 22, 11, 90]
print(quick_sort(numbers))  # Вывод: [11, 12, 22, 25, 34, 64, 90]